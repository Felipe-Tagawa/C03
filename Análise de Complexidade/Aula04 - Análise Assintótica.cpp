// Complexidade linear : n e complexidade proporcionais.
// Complexidade logarítmica : n aumenta e complexidade diminui(busca binária)
// Complexidade aninhada(quadrática) : complexidade aumenta bastante para aumento de n.
// Notações : O(Big Oh) - Pior caso, Omega - Melhor caso e Teta - caso Médio, duas curvas, uma abaixo e outra acima.
// Para o Big Oh(Pior caso), zerar os elementos sem n, deixar os coeficientes iguais a 1 e deixar apenas o elemento com maior expoente.
// Ex. : 3n^2 -100 n + 3 = 3n^2 - 100n = n^2 - n = n^2 = O(n^2).
// i = i + 2 --> n/2.
// k = k*2 --> n*log(n) na base n.
// log (n) na base 2 é o melhor caso, pois cresce pouco a cada aumento de n.
// log (n) < n < n*log(n) < n^2 < n^3 < 2^n < n! (mais tempo - decrescente).